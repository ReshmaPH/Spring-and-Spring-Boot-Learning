package com.sample.spring.project.Learnspringframework.game;

public class MarioGame {
	public void up() {
		System.out.println("jump");
	}
	
	public void down() {
		System.out.println("go in to a hole");
	}
	
	public void left() {
		System.out.println("stop");
	}
	
	public void right() {
		System.out.println("go faster");
	}
}
